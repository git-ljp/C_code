//计算100 以内的非负整数的加法计算,在进行大于等于100的正整数的计算时，规则如下：

//1.只保留该数的最后两位，例如：对KiKi来说1234等价于34；

//2.如果计算结果大于等于 100， 那么KIKI也仅保留计算结果的最后两位，如果此两位中十位为0，则只保留个位。
#include<stdio.h>
int main()
{
    int a = 0;
    int b = 0;
    scanf("%d %d", &a, &b);
    int ret = ((a % 100) + (b % 100)) % 100;
    printf("%d", ret);
    return 0;
}
